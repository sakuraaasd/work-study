#设计一个类 （类比生活中：设计一张登记表）
class Student:
    name: None
    gender: None
    nationality:None
    native_place:None
    age=None
#创建一个对象（类比生活中：打印一张登记表）
stu_1=Student()
#对象属性进行赋值（类比生活中：填写登记表）
stu_1.name = 'linjunjie'
stu_1.gender = 'nan'
stu_1.nationality = 'china'
stu_1.native_place = 'shandong'
stu_1.age = 31
#获取对象中记录的信息
print(stu_1.name)
print(stu_1.gender)
print(stu_1.nationality)
print(stu_1.native_place)
print(stu_1.age)

